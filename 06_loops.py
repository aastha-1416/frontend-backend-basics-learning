'''
syntax of for loop
for variable in iterable:
    # Code to be executed for each item in the iterable
'''
print ("for loop")

fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

'''
syntax of while loop
while condition:
    # Code to be executed
'''

print("While loop")
i = 1
while i <= 10:
   print(i)
   i += 1